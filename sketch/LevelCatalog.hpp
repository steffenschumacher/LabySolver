inline uint8_t parseHexMask(char c) { if(c>='0'&&c<='9') return c-'0'; if(c>='A'&&c<='F') return c-'A'+10; throw std::invalid_argument("invalid tile mask"); }
inline LevelDefinition loadLevel(size_t number) {
    if(number==0||number>LEVEL_CATALOG.size()) throw std::out_of_range("level number");
    const auto& r=LEVEL_CATALOG[number-1]; LevelDefinition l;
    for(uint8_t i=0;i<BOARD_CELLS;++i) l.initial.setTile(i,parseHexMask(r.board[i]));
    if(r.board[BOARD_CELLS]!='\0') throw std::logic_error("level board length");
    l.initial.setTile(SPARE_POSITION,r.spare); l.initial.setLadybug(r.start);
    for(uint8_t i=0;i<4;++i) l.initial.setGoal(i,r.goals[i]);
    l.initial.setNextGoal(0); l.initial.setDepth(0); l.maxPushes=r.pushes; l.mayPushPlayerOut=r.pushOut;
    validateLevel(l); normalizeGoals(l.initial); return l;
}
